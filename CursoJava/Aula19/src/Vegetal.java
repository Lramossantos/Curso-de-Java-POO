public class Vegetal {
    private boolean vivo;
    private int massa;

    public Vegetal(int massa){
        this.massa = massa;        
    }
    
    public boolean isVivo() {
        return vivo;
    }
    public void setVivo(boolean vivo) {
        this.vivo = vivo;
    }
    public int getMassa() {
        return massa;
    }
    public void setMassa(int massa) {
        this.massa = massa;
    }

    
}
